# Первая область ограничений 
g1(x) = -x[1]
g2(x) = -x[2]
g3(x) = x[1] + x[2] - 10.0
h11(x) = 1.0 / g1(x) + 1.0 / g2(x) + 1.0 / g3(x) # Внутренние штрафы
h12(x) = 0.25 * (g1(x) + abs(g1(x)))^2 + 0.25 * (g2(x) + abs(g2(x)))^2 + 0.25 * (g3(x) + abs(g3(x)))^2 # Внешние штрафы

# Вторая область ограничений для внутренних штрафов 
g4(x) = (x[1] + 3.0)^2 / 4.0 + (x[2] + 4)^2 / 9.0 - 10.0
h21(x) = 1.0 / g4(x) # Внутренние штрафы
h22(x) = 0.25 * (g4(x) + abs(g4(x)))^2 # Внешние штрафы


